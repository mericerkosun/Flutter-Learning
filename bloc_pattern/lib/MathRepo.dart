class MathRepo {

  int topla(int alinanSayi1, int alinanSayi2){
    int sayi1 = alinanSayi1;
    int sayi2 = alinanSayi2;
    int sonuc = sayi1 + sayi2;
    return sonuc;
  }

  int carp(int alinanSayi1, int alinanSayi2){
    int sayi1 = alinanSayi1;
    int sayi2 = alinanSayi2;
    int sonuc = sayi1 * sayi2;
    return sonuc;
  }
}